#include <stdio.h>

int main() {
    // แสดงค่าแบบทศนิยม 3 ตำแหน่ง จัดชิดขวาในพื้นที่ 6 หลัก และตามด้วยเครื่องหมายเปอร์เซ็นต์
    printf("a=%6.3f%%\n", 0.617);   // ผลลัพธ์: a= 0.617%

    // แสดงค่าเป็นจำนวนเต็ม จัดชิดขวาในพื้นที่ 4 หลัก และตามด้วยเครื่องหมายเปอร์เซ็นต์
    printf("b=%4d%%\n", 25);        // ผลลัพธ์: b=  25%

    // แสดงค่า 19.000 (แปลงจาก int เป็น float อัตโนมัติ) แบบทศนิยม 3 ตำแหน่ง ในพื้นที่ 6 หลัก
    printf("c  =%6.3f%%\n", 19);    // ผลลัพธ์: c  =19.000%

    // แสดงค่าเป็นจำนวนเต็ม จัดชิดซ้ายในพื้นที่ 2 หลัก และตามด้วยเครื่องหมายเปอร์เซ็นต์
    printf("d =%-2d%%\n", 9);       // ผลลัพธ์: d =9 %

    // แสดงค่าทศนิยม 3 ตำแหน่ง จัดชิดซ้ายในพื้นที่ 7 หลัก และตามด้วยเครื่องหมายเปอร์เซ็นต์
    printf("e=%-7.3f%%\n", 124.555); // ผลลัพธ์: e=124.555%

    // แสดงค่าทศนิยม 3 ตำแหน่ง จัดชิดซ้ายในพื้นที่ 5 หลัก และตามด้วยเครื่องหมายเปอร์เซ็นต์
    printf("f=%-5.3f%%\n", 0.001);   // ผลลัพธ์: f=0.001 %

    return 0; // จบโปรแกรม
}